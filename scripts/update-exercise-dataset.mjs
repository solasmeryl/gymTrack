import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";

const commit = process.env.EXERCISE_DATASET_COMMIT || "7455efae41b330c265e7cd4b78dfa848e7ce5ebd";
const repository = "hasaneyldrm/exercises-dataset";
const rawRoot = `https://raw.githubusercontent.com/${repository}/${commit}`;
const response = await fetch(`${rawRoot}/data/exercises.json`);
if (!response.ok) throw new Error(`Exercise dataset download failed: ${response.status}`);

const sourceExercises = await response.json();
const exercises = sourceExercises.map((exercise) => ({
  name: exercise.name,
  bodyPart: exercise.body_part,
  equipment: exercise.equipment,
  target: exercise.target,
  secondaryMuscles: exercise.secondary_muscles || [],
  steps: exercise.instruction_steps?.en || [],
  image: exercise.image,
  animation: exercise.gif_url,
}));

const output = `// Generated by scripts/update-exercise-dataset.mjs. Do not edit by hand.\n` +
  `export const EXERCISE_DATASET = ${JSON.stringify({ repository, commit, mediaRoot: rawRoot, attribution: sourceExercises[0]?.attribution || "© Gym visual — https://gymvisual.com/", count: exercises.length, uniqueNames: new Set(exercises.map((exercise) => exercise.name)).size })};\n` +
  `export const BUILT_IN_EXERCISES = ${JSON.stringify(exercises)};\n`;

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
await writeFile(path.join(projectRoot, "exercise-dataset.js"), output);
console.log(`Wrote ${exercises.length} exercises from ${repository}@${commit}.`);
